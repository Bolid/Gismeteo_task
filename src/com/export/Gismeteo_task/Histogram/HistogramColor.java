package com.export.Gismeteo_task.Histogram;


public class HistogramColor {

    private int RED, GREEN, BLUE;

    public void setColor(int RED, int GREEN, int BLUE){
        this.RED = RED;
        this.GREEN = GREEN;
        this.BLUE = BLUE;
    }

    public int getRED(){
        return RED;
    }

    public int getGREEN(){
        return GREEN;
    }

    public int getBLUE(){
        return BLUE;
    }
}
